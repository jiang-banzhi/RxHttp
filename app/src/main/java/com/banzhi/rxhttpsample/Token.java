public class Token {


    private String Token;

    public String getToken() {
        return Token;
    }

    public void setToken(String token) {
        Token = token;
    }

    @Override
    public String toString() {
        return "Token{" +
                "Token='" + Token + '\'' +
                '}';
    }
}
